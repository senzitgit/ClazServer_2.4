package com.senzit.cyberclaz.server.model;

public class Rebbon {
	
	private String rebbonId;
	private String rebbonLink;
	private ClassRoom classRoom;
	
	public Rebbon(){}

	public String getRebbonId() {
		return rebbonId;
	}

	public void setRebbonId(String rebbonId) {
		this.rebbonId = rebbonId;
	}

	public String getRebbonLink() {
		return rebbonLink;
	}

	public void setRebbonLink(String rebbonLink) {
		this.rebbonLink = rebbonLink;
	}

	public ClassRoom getClassRoom() {
		return classRoom;
	}

	public void setClassRoom(ClassRoom classRoom) {
		this.classRoom = classRoom;
	}

}
